
class Contacts{
    setId (id){ 
        this.id = id;
    }

    getId(){
        return this.id;
    }

    setEmail (email){
        this.email = email;
    }

    getEmail (){
        return this.email;
    }

    setFirstName (firstName){
        this.firstName = firstName
    }

    getFirstName (){
        return this.firstName;
    }

    setLastName (lastName){
        this.lastName = lastName;
    }

    getLastName (){
        return this.lastName;
    }

}

