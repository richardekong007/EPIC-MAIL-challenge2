
export class Sent {

    setSenderId (senderId){
        this.senderId = senderId;
    }

    getSenderId (){
        return this.senderId;
    }

    setMessageId (messageId){
        this.messageId = messageId;
    }

    getMessageId (){
        return this.messageId;
    }

    setCreatedOn (createdOn){
        this.createdOn = createdOn;
    }

    getCreatedOn (){
        return this.createdOn;
    }

}

